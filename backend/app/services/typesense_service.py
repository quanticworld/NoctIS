    async def _ensure_collection(self, schema: Dict[str, Any]) -> bool:
        """Ensure a collection exists, create if it doesn't"""
        try:
            # Check if collection exists
            self.client.collections[schema['name']].retrieve()
            logger.info(f"Collection '{schema['name']}' already exists")
            return True
        except ObjectNotFound:
            # Create collection
            try:
                self.client.collections.create(schema)
                logger.info(f"Collection '{schema['name']}' created successfully")
                return True
            except TypesenseClientError as e:
                logger.error(f"Failed to create collection '{schema['name']}': {e}")
                return False
        except Exception as e:
            logger.error(f"Error checking collection '{schema['name']}': {e}")
            return False

    async def get_collection_stats(self, collection_name: str = 'leaks') -> Optional[Dict[str, Any]]:
        """Get statistics for a collection"""
        try:
            collection = self.client.collections[collection_name].retrieve()
            return {
                'name': collection['name'],
                'num_documents': collection['num_documents'],
                'created_at': collection.get('created_at'),
                'num_memory_shards': collection.get('num_memory_shards', 0),
            }
        except ObjectNotFound:
            return None
        except Exception as e:
            logger.error(f"Error getting collection stats: {e}")
            return None

    async def search(
        self,
        query: str,
        search_fields: List[str],
        filter_by: Optional[str] = None,
        per_page: int = 20,
        page: int = 1,
        collection_name: str = 'leaks',
        typo_tolerance: bool = True,
        prefix: bool = True
    ) -> Dict[str, Any]:
        """
        Search in Typesense collection

        Args:
            query: Search query string
            search_fields: Fields to search in
            filter_by: Filter expression (e.g., "breach_name:=LinkedIn")
            per_page: Results per page
            page: Page number
            collection_name: Collection to search in
            typo_tolerance: Enable typo tolerance
            prefix: Enable prefix search

        Returns:
            Search results with hits and facets
        """
        try:
            search_params = {
                'q': query,
                'query_by': ','.join(search_fields),
                'per_page': per_page,
                'page': page,
                'typo_tokens_threshold': 1 if typo_tolerance else 0,
                'prefix': prefix,
            }

            if filter_by:
                search_params['filter_by'] = filter_by

            results = self.client.collections[collection_name].documents.search(search_params)

            return {
                'hits': results.get('hits', []),
                'found': results.get('found', 0),
                'out_of': results.get('out_of', 0),
                'page': page,
                'search_time_ms': results.get('search_time_ms', 0),
                'facet_counts': results.get('facet_counts', []),
            }

        except Exception as e:
            logger.error(f"Search error: {e}")
            return {
                'hits': [],
                'found': 0,
                'error': str(e)
            }

    async def multi_search(self, searches: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
        """
        Execute multiple searches in a single request

        Args:
            searches: List of search parameters

        Returns:
            List of search results
        """
        try:
            search_requests = {
                'searches': [
                    {
                        'collection': search.get('collection', 'leaks'),
                        'q': search['query'],
                        'query_by': search['query_by'],
                        **{k: v for k, v in search.items() if k not in ['collection', 'query', 'query_by']}
                    }
                    for search in searches
                ]
            }

            results = self.client.multi_search.perform(search_requests, {})
            return results.get('results', [])

        except Exception as e:
            logger.error(f"Multi-search error: {e}")
            return []

    async def import_documents(
        self,
        documents: List[Dict[str, Any]],
        collection_name: str = 'leaks',
        action: str = 'upsert'
    ) -> Dict[str, Any]:
        """
        Import documents in batch

        Args:
            documents: List of documents to import
            collection_name: Target collection
            action: Import action (upsert, create, update)

        Returns:
            Import results with success/failure counts
        """
        if not documents:
            return {'success': 0, 'failed': 0, 'errors': []}

        try:
            # Import in batches
            results = self.client.collections[collection_name].documents.import_(
                documents,
                {'action': action}
            )

            # Parse results
            success_count = sum(1 for r in results if r.get('success', False))
            failed_count = len(results) - success_count
            errors = [r for r in results if not r.get('success', False)]

            return {
                'success': success_count,
                'failed': failed_count,
                'errors': errors[:10]  # Return first 10 errors
            }

        except Exception as e:
            logger.error(f"Import error: {e}")
            return {
                'success': 0,
                'failed': len(documents),
                'error': str(e)
            }

    async def delete_collection(self, collection_name: str) -> bool:
        """Delete a collection"""
        try:
            self.client.collections[collection_name].delete()
            logger.info(f"Collection '{collection_name}' deleted")
            return True
        except Exception as e:
            logger.error(f"Error deleting collection: {e}")
            return False

    async def get_facets(
        self,
        collection_name: str = 'leaks',
        facet_fields: List[str] = None
    ) -> Dict[str, List[Dict[str, Any]]]:
        """
        Get facet counts for specified fields

        Args:
            collection_name: Collection to query
            facet_fields: Fields to get facets for

        Returns:
            Dictionary of facet counts by field
        """
        if facet_fields is None:
            facet_fields = ['breach_name', 'domain', 'breach_date']

        try:
            search_params = {
                'q': '*',
                'query_by': 'email',
                'facet_by': ','.join(facet_fields),
                'per_page': 0,  # We only want facets, not results
            }

            results = self.client.collections[collection_name].documents.search(search_params)

            facets = {}
            for facet in results.get('facet_counts', []):
                field_name = facet['field_name']
                facets[field_name] = facet['counts']

            return facets

        except Exception as e:
            logger.error(f"Error getting facets: {e}")
            return {}


