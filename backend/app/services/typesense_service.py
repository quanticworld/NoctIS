"""Typesense service for indexing and searching OSINT data"""
import typesense
from typesense.exceptions import ObjectNotFound, TypesenseClientError
from typing import Optional, Dict, List, Any
import logging
from app.config import settings

logger = logging.getLogger(__name__)


class TypesenseService:
    """Service for managing Typesense collections and search operations"""

    def __init__(self):
        """Initialize Typesense client"""
        self._client: Optional[typesense.Client] = None
        self._initialized = False

    @property
    def client(self) -> typesense.Client:
        """Get or create Typesense client (lazy initialization)"""
        if self._client is None:
            self._client = typesense.Client({
                'api_key': settings.typesense_api_key,
                'nodes': [{
                    'host': settings.typesense_host,
                    'port': settings.typesense_port,
                    'protocol': settings.typesense_protocol
                }],
                'connection_timeout_seconds': settings.typesense_connection_timeout
            })
        return self._client

    async def health_check(self) -> Dict[str, Any]:
        """Check Typesense server health"""
        try:
            health = self.client.operations.perform('GET', '/health')
            # Typesense returns {"ok": true}
            if isinstance(health, dict) and health.get('ok'):
                return {"status": "healthy", "typesense": "ok"}
            elif isinstance(health, str) and health == 'ok':
                return {"status": "healthy", "typesense": "ok"}
            return {"status": "healthy", "details": health}
        except Exception as e:
            logger.error(f"Typesense health check failed: {e}")
            return {"status": "unhealthy", "error": str(e)}

